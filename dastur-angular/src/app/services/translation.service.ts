@Injectable({ providedIn: 'root' })
export class TranslationService {
  currentLang = signal<Lang>('ru');

  get(key: string): string {
    return data[this.currentLang()][key] ?? key;
  }

  setLang(lang: Lang) {
    this.currentLang.set(lang);
    localStorage.setItem('dastur_lang', lang);
  }

  init() {
    const saved = localStorage.getItem('dastur_lang') as Lang;
    if (saved && ['kz', 'ru', 'en'].includes(saved)) {
      this.currentLang.set(saved);
    }
  }
}
