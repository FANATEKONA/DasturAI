@Injectable({ providedIn: 'root' })
export class TranslationService {
  private langSubject = new BehaviorSubject<Lang>('ru');
  lang$ = this.langSubject.asObservable();

  constructor() {
    try {
      const saved = localStorage.getItem('dastur_lang') as Lang;
      if (saved && ['kz', 'ru', 'en'].includes(saved)) {
        this.langSubject.next(saved);
      }
    } catch {}
  }

  setLanguage(lang: Lang): void {
    this.langSubject.next(lang);
    try {
      localStorage.setItem('dastur_lang', lang);
    } catch {}
  }

  t(key: string): string {
    return translations[this.langSubject.value][key] ?? key;
  }

  get currentLang(): Lang {
    return this.langSubject.value;
  }
}
